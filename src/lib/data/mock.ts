// Helper Functions
export const getPropertyById = (id: string): Property | undefined => {
  return mockProperties.find((property) => property.id === id);
};

export const getPropertiesByCity = (city: string): Property[] => {
  return mockProperties.filter(
    (property) => property.city.toLowerCase() === city.toLowerCase()
  );
};

export const getFeaturedProperties = (): Property[] => {
  return mockProperties.filter((property) => property.featured);
};

export const getVerifiedProperties = (): Property[] => {
  return mockProperties.filter((property) => property.verified);
};

export const calculateDiscountedPrice = (
  basePrice: number,
  plan: PaymentPlan
): { totalPrice: number; discount: number; monthlyEquivalent: number } => {
  const planOption = paymentPlanOptions.find((p) => p.plan === plan);
  if (!planOption) {
    return { totalPrice: basePrice, discount: 0, monthlyEquivalent: basePrice };
  }

  const totalMonths = planOption.totalMonths;
  const discount = planOption.discount;
  const totalPrice = basePrice * totalMonths * (1 - discount / 100);
  const monthlyEquivalent = totalPrice / totalMonths;

  return {
    totalPrice: Math.round(totalPrice),
    discount,
    monthlyEquivalent: Math.round(monthlyEquivalent),
  };
};

